#' 翻译函数：根据 key 和语言返回对应文本
#' @param key 翻译键
#' @param lang 语言代码 ("en" 或 "zh")
#' @return 翻译后的字符串
tr <- function(key, lang = "en") {
  if (is.null(lang) || !lang %in% names(TRANS)) lang <- "en"
  val <- TRANS[[lang]][[key]]
  if (is.null(val)) val <- TRANS[["en"]][[key]]
  if (is.null(val)) key else val
}
