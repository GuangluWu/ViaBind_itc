graph_normalize_lang <- function(lang = "en") {
  lang_chr <- tolower(as.character(if (is.null(lang)) "en" else lang)[1])
  if (identical(lang_chr, "zh")) "zh" else "en"
}

graph_tr <- function(key, lang = "en") {
  lang_norm <- graph_normalize_lang(lang)
  val <- GRAPH_TRANS[[lang_norm]][[key]]
  if (is.null(val)) {
    val <- GRAPH_TRANS[["en"]][[key]]
  }
  if (is.null(val)) key else val
}

graph_trf <- function(key, lang = "en", ...) {
  tpl <- graph_tr(key, lang = lang)
  args <- list(...)
  if (length(args) == 0) return(tpl)
  tryCatch(do.call(sprintf, c(list(tpl), args)), error = function(e) tpl)
}
